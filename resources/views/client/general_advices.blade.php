                        <h4>Tipping</h4>
                        <p>In Morocco, Staff expects tips from tourists after the tours. The best way to tip the
                            accompanying team is to make contributions at the end of travel and provide an envelope for
                            each. There’s no set amount as this often depends on group size, but as a guideline around
                            £20
                            (or 300 Dirhams).</p>
                        <h4>Shopping</h4>
                        <p>When shopping for souvenirs in the souq (markets) it is normal to haggle. Don’t be alarmed by
                            the
                            shopkeeper’s inflated starting price as you can usually hope to finish up at about half of
                            this.
                            It can be a frustrating experience but it always pays off to stay calm and good humoured and
                            the
                            general idea is to come away feeling that you got good value for your purchase. In other
                            words
                            there’s no right or wrong price, just a price that suits both parties and everyone goes away
                            happy. In upmarket boutiques haggling is not appropriate.</p>
